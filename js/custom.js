const bookList = document.getElementById("book-list");

// Create the book element
function createBookElement(title, quantity, imageSrc) {
  const colElement = document.createElement("div");
  colElement.classList.add("col-lg-3", "col-md-6", "col-sm-8");

  const publicationElement = document.createElement("div");
  publicationElement.classList.add("singel-publication");

  const imageElement = document.createElement("div");
  imageElement.classList.add("image");
  const img = document.createElement("img");
  img.src = imageSrc;
  img.alt = "Publication";
  imageElement.appendChild(img);

  const contElement = document.createElement("div");
  contElement.classList.add("cont");

  const nameElement = document.createElement("div");
  nameElement.classList.add("name");
  const titleLink = document.createElement("a");
  titleLink.href = "#";
  const titleHeading = document.createElement("h6");
  titleHeading.textContent = title;
  titleLink.appendChild(titleHeading);
  const quantitySpan = document.createElement("span");
  quantitySpan.textContent = `${quantity} Book`;
  nameElement.appendChild(titleLink);
  nameElement.appendChild(quantitySpan);

  const buttonElement = document.createElement("div");
  buttonElement.classList.add("button", "text-right");
  const bookNowLink = document.createElement("a");
  bookNowLink.href = "#";
  bookNowLink.classList.add("main-btn");
  bookNowLink.textContent = "Booking Now";
  buttonElement.appendChild(bookNowLink);

  contElement.appendChild(nameElement);
  contElement.appendChild(buttonElement);

  publicationElement.appendChild(imageElement);
  publicationElement.appendChild(contElement);

  colElement.appendChild(publicationElement);

  return colElement;
}

